///////////////////////////////////////////////////////////////////////////////
/// @file menu_item.cpp
///
/// @brief Implementation for menu_item.
///////////////////////////////////////////////////////////////////////////////
#include "menu_item.hpp"

///////////////////////////////////////////////////////////////////////////////
/// @brief Constructor for MenuItem.
///
/// @param [in] item_name The name of this menu item.
/// @param [in] item_help_text The help text for this menu item.
///////////////////////////////////////////////////////////////////////////////
MenuItem::MenuItem(std::string item_name, std::string item_help_text)
{
    this->m_name = item_name;
    this->m_help = item_help_text;
}

///////////////////////////////////////////////////////////////////////////////
/// @brief Destroyer for MenuItem.
///////////////////////////////////////////////////////////////////////////////
MenuItem::~MenuItem()
{
    /* Does nothing. */
}

///////////////////////////////////////////////////////////////////////////////
/// @brief Getter for MenuItem name.
///
/// @returns The name of this menu item.
///////////////////////////////////////////////////////////////////////////////
const std::string MenuItem::name()
{
    return this->m_name;
}

///////////////////////////////////////////////////////////////////////////////
/// @brief Getter for MenuItem help text.
///
/// @returns The help text of this menu item.
///////////////////////////////////////////////////////////////////////////////
const std::string MenuItem::help()
{
    return this->m_help;
}
